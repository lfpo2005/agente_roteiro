package br.com.devluisoliveira.agenteroteiro.core.domain.entity.enums;

public enum RoleType {
    ROLE_USER,              // Usuário padrão com acesso básico ao sistema.
    ROLE_BOX,               // Operador de caixa, responsável por transações e vendas.
    ROLE_SECRETARY,         // Secretário(a), auxilia em tarefas administrativas e organizacionais.
    ROLE_MANAGER,           // Gerente, responsável por liderar equipes e supervisionar operações.
    ROLE_OWNER,             // Proprietário, tem controle total sobre o sistema e operações.
    ROLE_ADMIN,             // Administrador, gerencia o sistema e tem permissões avançadas.
    ROLE_HR_MANAGER,        // Gerente de RH, responsável por recrutamento, treinamento e gestão de funcionários.
    ROLE_ACCOUNTANT,        // Contador, cuida das finanças e da contabilidade da empresa.
    ROLE_SALES_MANAGER,     // Gerente de vendas, coordena a equipe de vendas e define estratégias comerciais.
    ROLE_SALES_REP,         // Representante de vendas, realiza vendas e negociações diretamente com clientes.
    ROLE_CASHIER,           // Caixa, realiza operações de pagamento e controle de dinheiro.
    ROLE_MARKETING,         // Profissional de marketing, desenvolve e executa campanhas publicitárias.
    ROLE_LOGISTICS,         // Responsável pela logística, cuida do transporte, estoque e distribuição de produtos.
    ROLE_TECH_SUPPORT,      // Suporte técnico, resolve problemas de TI e oferece assistência aos usuários.
    ROLE_DEVELOPER,         // Desenvolvedor, cria e mantém o software da empresa.
    ROLE_CUSTOMER_SERVICE,  // Atendimento ao cliente, oferece suporte e resolve problemas dos clientes.
    ROLE_INTERN,            // Estagiário, realiza atividades sob supervisão, em fase de aprendizado.
    ROLE_ANALYST,           // Analista, realiza estudos, relatórios e interpreta dados para auxiliar em decisões.
    ROLE_AUDITOR,           // Auditor, verifica e fiscaliza processos internos e financeiros.
    ROLE_PRODUCT_MANAGER,   // Gerente de produto, lidera o desenvolvimento e estratégia de produtos.
    ROLE_PURCHASER,         // Comprador, realiza compras e negocia com fornecedores.
    ROLE_STOCK_MANAGER,     // Gerente de estoque, controla e organiza o inventário de produtos.
    ROLE_OPERATOR,          // Operador, executa operações técnicas ou em linhas de produção.
    ROLE_TRAINER,           // Treinador, capacita funcionários e promove treinamentos internos.
    ROLE_SUPERVISOR         // Supervisor, supervisiona equipes e garante o cumprimento de processos e metas.
}

